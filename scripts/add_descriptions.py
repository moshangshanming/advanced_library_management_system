# 更新书籍简介
updated = 0
for book in books:
    title = book['title']
    author = book['author'] if 'author' in book else ''
    category = book['category'] if 'category' in book else ''
    
    if title in descriptions:
        desc = descriptions[title]
    elif category == '计算机':
        desc = f'《{title}》是一本计算机领域的专业书籍，内容涵盖相关技术知识，适合计算机专业学生和从业者阅读。'
